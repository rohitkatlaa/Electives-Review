function my_hide(){
	$(".basic").hide();
	$(".course_des").hide();
	$(".course_content").hide();
	$(".text_and_ref").hide();
	$(".syllabus").hide();
	$(".notes").hide();
	$(".assignments").hide();
	$(".projects").hide();
}
function basic(){
	my_hide();
	$(".basic").show();
}
function course_des(){
        my_hide();
        $(".course").show();
}
function course_content(){
        my_hide();
        $(".course_content").show();
}
function text_and_ref(){
        my_hide();
        $(".text_and_ref").show();
}
function syllabus(){
        my_hide();
        $(".syllabus").show();
}
function notes(){
        my_hide();
        $(".notes").show();
}
function assignments(){
        my_hide();
        $(".assignments").show();
}
function projects(){
        my_hide();
        $(".projects").show();
}
